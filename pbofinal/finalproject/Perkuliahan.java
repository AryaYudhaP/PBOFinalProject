    private void connectAndFillTable() {
    String url = "jdbc:mysql://localhost:3306/db_akademik";
    String user = "root"; // Ganti dengan username database Anda
    String password = ""; // Ganti dengan password database Anda

    String query = "SELECT Nim, Kode_MK, Nip, Nilai FROM perkuliahan";

    try {
        Connection conn = DriverManager.getConnection(url, user, password);
        Statement stmt = conn.createStatement();
        ResultSet rs = stmt.executeQuery(query);

        // Model tabel untuk menampung data dari database
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // Bersihkan tabel sebelum mengisi

        while (rs.next()) {
            // Tambahkan baris ke model tabel
            model.addRow(new Object[]{
                rs.getString("Nim"),
                rs.getString("Kode_MK"),
                rs.getString("Nip"),
                rs.getString("Nilai")
            });
        }
    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(null, "Gagal menghubungkan ke database: " + e.getMessage());
    }
}

