    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {                                        
        // TODO add your handling code here:
        String nim = jtfNim.getText();
        String kodeMK = jtfKodeMatakuliah.getText();
        String nip = jtfNip.getText();
        String nilai = jtfNilai.getText();

        if (nim.isEmpty() || kodeMK.isEmpty() || nip.isEmpty() || nilai.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Data tidak boleh kosong");
            return;
        }
    
        String url = "jdbc:mysql://localhost:3306/db_akademik";
        String user = "root"; // Ganti dengan username database Anda
        String password = ""; // Ganti dengan password database Anda
    
        String query = "UPDATE perkuliahan SET Kode_MK = ?, Nip = ?, Nilai = ? WHERE Nim = ?";
    
        try (Connection conn = DriverManager.getConnection(url, user, password);
             PreparedStatement pstmt = conn.prepareStatement(query)) {
            
            pstmt.setString(1, kodeMK);
            pstmt.setString(2, nip);
            pstmt.setString(3, nilai);
            pstmt.setString(4, nim);
            
            int affectedRows = pstmt.executeUpdate();
            
            if (affectedRows > 0) {
                JOptionPane.showMessageDialog(null, "Data berhasil diperbarui");
                connectAndFillTable(); // Memperbarui tabel
            } else {
                JOptionPane.showMessageDialog(null, "Data gagal diperbarui");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Gagal menghubungkan ke database: " + e.getMessage());
        }
        clearData();
    }                                       


