    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {
        // Mendapatkan model dari tabel
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    
        // Mendapatkan indeks baris yang dipilih
        int selectedRowIndex = jTable1.getSelectedRow();
    
        // Mengatur nilai dari tabel ke dalam field teks
        jtfNim.setText(model.getValueAt(selectedRowIndex, 0).toString());
        jtfNamaMahasiswa.setText(model.getValueAt(selectedRowIndex, 1).toString());
        jtfTanggalLahir.setText(model.getValueAt(selectedRowIndex, 2).toString());
        jtfAlamat.setText(model.getValueAt(selectedRowIndex, 3).toString());
        
        // Mengatur checkbox untuk jenis kelamin
        String jenisKelamin = model.getValueAt(selectedRowIndex, 4).toString();
        if (jenisKelamin.equals("Laki-laki")) {
            jCheckBox1.setSelected(true);
            jCheckBox2.setSelected(false);
        } else {
            jCheckBox1.setSelected(false);
            jCheckBox2.setSelected(true);
        }
    }                                          

