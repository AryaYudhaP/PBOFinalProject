    private void loadMahasiswaData() {
        try {
            // Membuat koneksi ke database
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/db_akademik", "root", "");
            Statement stmt = con.createStatement();
            
            // Query untuk mengambil data mahasiswa
            String sql = "SELECT Nim, Nama_Mhs, Tgl_Lahir, Alamat, Jenis_Kelamin FROM mahasiswa";
            ResultSet rs = stmt.executeQuery(sql);
            
            // Mengatur model tabel dan mengisinya dengan data dari ResultSet
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0); // Mengosongkan tabel sebelum diisi
            
            while (rs.next()) {
                // Menambahkan baris ke tabel
                model.addRow(new Object[]{
                    rs.getString("Nim"),
                    rs.getString("Nama_Mhs"),
                    rs.getDate("Tgl_Lahir"),
                    rs.getString("Alamat"),
                    rs.getString("Jenis_Kelamin")
                });
            }
            
            // Menutup koneksi
            rs.close();
            stmt.close();
            con.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }


